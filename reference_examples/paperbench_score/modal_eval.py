@APP.local_entrypoint()
def main(
    agent_file: str,
    paper_id: str = "sequential-neural-score-estimation",
    agent_time_limit: int = 600,
    reproduction_timeout: int = 600,
    gpu: str = "A10G",
):
    extra = {
        "PAPER_ID": paper_id,
        "AGENT_TIME_LIMIT": str(agent_time_limit),
        "REPRODUCTION_TIMEOUT": str(reproduction_timeout),
    }
    sb = modal.Sandbox.create(
        "bash",
        "-lc",
        RUN,
        app=APP,
        image=image(agent_file),
        secrets=[modal.Secret.from_dict(load_env(extra))],
        timeout=60 * 60,
        gpu=gpu,
    )
    for line in sb.stdout:
        print(line, end="")
    err = sb.stderr.read()
    result = sb.wait()
    rc = getattr(result, "returncode", getattr(sb, "returncode", 0))
    print(err, end="")
    raise SystemExit(rc)
