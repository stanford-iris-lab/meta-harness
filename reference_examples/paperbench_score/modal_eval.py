def load_env(extra: dict[str, str] | None = None) -> dict[str, str]:
    env = {k: v for k, v in os.environ.items() if k.endswith("_API_KEY") or k == "HF_TOKEN"}
    for dotenv in [PB_ROOT / ".env", PAPERS_ROOT / ".env", Path.home() / "Projects" / "aster" / ".env"]:
        if dotenv.exists():
            for line in dotenv.read_text().splitlines():
                line = line.strip()
                if line and not line.startswith("#") and "=" in line:
                    k, v = (x.strip() for x in line.split("=", 1))
                    if k.endswith("_API_KEY") or k == "HF_TOKEN":
                        env[k] = v.strip("'\"")
    env.setdefault("GRADER_OPENAI_API_KEY", env.get("OPENAI_API_KEY", ""))
    if extra:
        env.update(extra)
    if not env.get("OPENAI_API_KEY"):
        raise RuntimeError("OPENAI_API_KEY missing")
    return env


def image(agent_file: str) -> modal.Image:
    dockerfile = Path(tempfile.gettempdir()) / "paperbench-score-modal.Dockerfile"
    dockerfile.write_text(
        (PB_ROOT / "paperbench/Dockerfile.base")
        .read_text()
        .replace("ENV DEBIAN_FRONTEND=\n", 'ENV DEBIAN_FRONTEND=""\n')
    )
    return (
        modal.Image.from_dockerfile(dockerfile, context_dir=PB_ROOT)
        .apt_install("git-lfs")
        .run_commands("curl -LsSf https://astral.sh/uv/install.sh | sh", "mkdir -p /work/papers")
        .add_local_file(str(PAPERS_ROOT / "bare_runtime.py"), "/work/papers/bare_runtime.py", copy=True)
        .add_local_dir(str(ROOT / "agents"), "/work/papers/agents", copy=True)
        .add_local_file(str(Path(agent_file).resolve()), "/work/papers/basic_agent.py", copy=True)
    )


